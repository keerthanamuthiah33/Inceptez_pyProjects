# Basic variable declarations
x = 10          # integer
name = "Alice"  # string
price = 99.99   # float
is_active = True # boolean

# You can also assign multiple variables at once
a, b, c = 1, 2, 3

# Assign the same value to multiple variables
x = y = z = 0

# Variables can change type since Python is dynamically typed
x = 42
x = "Now I'm a string"
